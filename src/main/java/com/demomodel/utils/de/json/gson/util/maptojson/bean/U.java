package com.demomodel.utils.de.json.gson.util.maptojson.bean;
public class U {
	private String name;
	private String pwd;
	private Integer age;

	public U() {
		super();
	}

	public U(String name, String pwd, Integer age) {
		super();
		this.name = name;
		this.pwd = pwd;
		this.age = age;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

}